export function isBasicType(val = ''): boolean {
  const t = Object.prototype.toString.call(val)
  if (t === '[object String]'
  || t === '[object Boolean]'
  || t === '[object Number]'
  || t === '[object Null]'
  || t === '[object Undefined]'
  || t === '[object Symbol]')
    return true

  return false
}
